public class TestdataAgai {

    public static void main(String[] args){



        int x = 1;

        System.out.println(x); //1  true

        System.out.println(++x);   //2 true

        System.out.println(x++);   //2 sop(x=2) ,then x become 3 ; true
        System.out.println(--x);   //2 true

        System.out.println(x--);   //2  sop(x=2) ,x become 1 true

        System.out.println(x); //1 true




    }
}
