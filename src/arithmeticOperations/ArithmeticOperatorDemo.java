package arithmeticOperations;

public class ArithmeticOperatorDemo {

    public static void main(String[] args){

        int num1 =46;
        int num2 = 22;



        System.out.println(" 46 + 22 : " + (num1 + num2) );
        System.out.println(" 46 - 22 : " + (num1 -num2));
        System.out.println(" 46 * 22 : " + (num1 * num2));
        System.out.println(" 46 / 22 : " + (num1/num2));
        System.out.println(" 46 % 22 : " + (num1%num2 ));
    }
}
