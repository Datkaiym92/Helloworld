package arithmeticOperations;

public class InDecEx1 {
    public static void main(String args[])
    {


        int x=1;
        System.out.println(x); //1  true

        x++;

        System.out.println(x); //2  true

        ++x;

        System.out.println(x); //3  true

        int y=3;

        System.out.println(y); //3 true

        --y;

        System.out.println(y);  //2 true

        y--;

        System.out.println(y);  //1 true













    }
}
